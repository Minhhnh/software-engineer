# Single and double quotes
my_string = "Hello"
my_string = "Hello"
my_string = "I'm a 'Geek'"
print(my_string)

# Escape characters
my_string = 'I\'m a "Geek"'
print(my_string)

# Multiline string
my_string = """Hello
World"""
print(my_string)

# Backslash to continue on next line
my_string = "Hello \
World"

print(my_string)
